var createError = require('http-errors');
var express = require('express');
var path = require('path');
var cookieParser = require('cookie-parser');
var logger = require('morgan');

var indexRouter = require('./routes/index');
var faasRouter = require('./routes/faas');
var projectsRouter = require('./routes/projects');
var toolsRouter = require('./routes/tools');
var researchersRouter = require('./routes/researchers');
var conferencesRouter = require('./routes/conferences');
var loginRouter = require('./routes/login');

var app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'pug');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));
app.use('/css', express.static(__dirname + '/node_modules/bootstrap/dist/css'));
app.use('/js', express.static(__dirname + '/node_modules/bootstrap/dist/js'));
app.use('/js', express.static(__dirname + '/node_modules/jquery/dist'));

app.use('/', indexRouter);
app.use('/:lang/faas', faasRouter);
app.use('/:lang/projects', projectsRouter);
app.use('/:lang/tools', toolsRouter);
app.use('/:lang/researchers', researchersRouter);
app.use('/:lang/conferences', conferencesRouter);
app.use('/:lang/login', loginRouter);


// catch 404 and forward to error handler
app.use(function(req, res, next) {
  next(createError(404));
});

// error handler
app.use(function(err, req, res, next) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;
